const mongoose = require("mongoose");
const userdata = new mongoose.Schema({
    id: {
        type: Number,
        required: true,
        unique: true
    },
    name: {
        type: String,
        required: true
    },
    age: {
        type: Number,
        required: true
    },
    gender: {
        type: String,
        required: true
    }

})



module.exports = mongoose.model("User", userdata, "userdata")